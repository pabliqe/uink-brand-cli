/**
 * Main meta files generation function
 */
export async function generateMetaFiles(brandData, generateDir, assetDir, refs = {}) {
  await mkdir(generateDir, { recursive: true })

  // Normalize asset path: always starts with /, no trailing slash (unless it is the root)
  const assetPath = assetDir === '/' ? '' : '/' + assetDir.replace(/^\.?\//, '').replace(/\/$/, '')

  const resolvedRefs = {
    ogImage: refs.ogImage || 'og-image.png',
    faviconPrimary: refs.faviconPrimary || 'favicon.ico',
    hasFaviconSvg: Boolean(refs.hasFaviconSvg),
  }

  await generateReactComponent(brandData, path.join(generateDir, 'BrandMeta.jsx'), assetPath, resolvedRefs)
  console.log(`   ✓ BrandMeta.jsx`)

  await generateTypeScriptComponent(brandData, path.join(generateDir, 'BrandMeta.tsx'), assetPath, resolvedRefs)
  console.log(`   ✓ BrandMeta.tsx`)

  await generateHTMLPartial(brandData, path.join(generateDir, 'meta.html'), assetPath, resolvedRefs)
  console.log(`   ✓ meta.html`)

  await generateNextMetadata(brandData, path.join(generateDir, 'next-metadata.ts'), assetPath, resolvedRefs)
  console.log(`   ✓ next-metadata.ts`)

  // Generate README in the output directory
  const readme = `# Auto-Generated Brand Assets

This directory contains auto-generated meta tag components and snippets.

**DO NOT EDIT THESE FILES MANUALLY** - They will be overwritten on the next build.

To regenerate: \`npx uink-brand\`

## Usage

### React/Next.js (JSX)
\`\`\`jsx
import BrandMeta from './.og-brand/BrandMeta.jsx'

export default function App() {
  return (
    <>
      <head>
        <BrandMeta />
      </head>
      <body>...</body>
    </>
  )
}
\`\`\`

### Next.js App Router
\`\`\`typescript
import { metadata } from './.og-brand/next-metadata'
export { metadata }
\`\`\`

### Static HTML
Copy the contents of \`meta.html\` into your \`<head>\` tag.

## Generated Files
- \`BrandMeta.jsx\` - React component
- \`BrandMeta.tsx\` - TypeScript React component
- \`next-metadata.ts\` - Next.js metadata object
- \`meta.html\` - Static HTML snippet
`

  await writeFile(path.join(generateDir, 'README.md'), readme)
  console.log(`   ✓ README.md`)
}

/**
 * Derive MIME type from an image filename extension
 */
function mimeTypeFromExt(filename) {
  const ext = (filename || '').split('.').pop().toLowerCase()
  const map = { jpg: 'image/jpeg', jpeg: 'image/jpeg', png: 'image/png', webp: 'image/webp', gif: 'image/gif' }
  return map[ext] || 'image/jpeg'
}

/**
 * Escape utilities
 */
function escapeHtml(text) {
  if (!text) return ''
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}

function escapeJsx(text) {
  if (!text) return ''
  return text
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\n/g, '\\n')
}
