/**
 * Meta Tags and Manifest Generator
 * Creates PWA manifest, React components, and HTML partials
 */

import { mkdir, writeFile } from 'fs/promises'
import path from 'path'

/**
 * Generate PWA manifest.json
 */
export async function generateManifest(brandData, outDir) {
  const displayName = brandData.name || brandData.siteTitle
  const manifest = {
    name: displayName,
    short_name: displayName.length > 12 ? displayName.substring(0, 12) : displayName,
    description: brandData.description,
    start_url: '/',
    display: 'standalone',
    background_color: brandData.colors.background,
    theme_color: brandData.colors.primary,
    orientation: 'portrait-primary',
    icons: [
      {
        src: '/icon-192x192.png',
        sizes: '192x192',
        type: 'image/png',
        purpose: 'any'
      },
      {
        src: '/icon-512x512.png',
        sizes: '512x512',
        type: 'image/png',
        purpose: 'any'
      },
      {
        src: '/icon-512x512-maskable.png',
        sizes: '512x512',
        type: 'image/png',
        purpose: 'maskable'
      }
    ]
  }

  const manifestPath = path.join(outDir, 'manifest.json')
  await writeFile(manifestPath, JSON.stringify(manifest, null, 2))
}

